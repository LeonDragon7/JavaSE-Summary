package com.oop.advanced.static_;

public class VisitStatic {
    /*
    什么是类变量
    类变量也叫静态变量/静态属性，是该类的所有对象共享的变量,任何一个该类的对象去
    访问它时,取到的都是相同的值,同样任何一个该类的对象去修改它时,修改的也是同一个
    变量。

    如何定义类变量
    定义语法:
    访问修饰符static数据类型变量名;[推荐]
    static访问修饰符数据类型变量名;

    如何访问类变量
    类名.类变量名
    或者对象名.类变量名【静态变量的访问修饰符的访问权限和范围和普通属性是一样的】
    推荐使用:类名.类变量名;
     */
}
